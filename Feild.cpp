void Feild::draw_map(int map[30][30]) {
	int i;
	int j;

	for (i = 0; i < 30; i++) {
		for (j = 0; j < 30; j++) {
			if (map[i][j] == WALL) {
				cout << "＊";
			}
			if (map[i][j] == NON) {
				cout << "　";
			}
		}
		cout << endl;
	}
}

int Feild::move_map(int* a, int id) {
	switch (id)
	{
	case FELES:
	case FELES_EAST:
		if (a[0] == 14 && a[1] == 29) {
			return FELES_HIGHWAY;
		}
		else if (a[0] == 0 && a[1] == 14) {
			return PIES;
		}
		else if (a[0] == 29 && a[1] == 14) {
			return FELES;
		}
	default:
		return id;
		break;
	}
}

